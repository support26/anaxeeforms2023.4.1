<template>
  <div id="dataset-show">
    <page-back v-show="dataExists" :to="[projectPath(), projectPath('entity-lists')]">
      <template #title>{{ project.dataExists ? project.nameWithArchived : '' }}</template>
      <template #back>{{ $t('resource.entities') }}</template>
    </page-back>
    <page-head v-show="dataExists">
      <template #title>
        {{ datasetName }}
      </template>
      <template #tabs>
        <li :class="tabClass('')" role="presentation">
          <router-link :to="tabPath('')">
            {{ $t('common.tab.overview') }}
          </router-link>
        </li>
        <li :class="tabClass('entities')" role="presentation">
          <router-link :to="tabPath('entities')">
            {{ $t('common.data') }}
          </router-link>
        </li>
        <li v-if="canRoute(tabPath('settings'))" :class="tabClass('settings')" role="presentation">
          <router-link :to="tabPath('settings')">
            {{ $t('common.tab.settings') }}
          </router-link>
        </li>
      </template>
    </page-head>
    <page-body>
      <loading :state="initiallyLoading"/>
      <router-view v-show="dataExists"/>
    </page-body>
  </div>
</template>

<script>
import Loading from '../loading.vue';
import PageBack from '../page/back.vue';
import PageBody from '../page/body.vue';
import PageHead from '../page/head.vue';

import useRoutes from '../../composables/routes';
import useTabs from '../../composables/tabs';
import { useRequestData } from '../../request-data';
import { apiPaths } from '../../util/request';
import { noop } from '../../util/util';

export default {
  name: 'DatasetShow',
  components: {
    Loading,
    PageBack,
    PageBody,
    PageHead
  },
  props: {
    projectId: {
      type: String,
      required: true
    },
    datasetName: {
      type: String,
      required: true
    }
  },
  setup() {
    // The component does not assume that this data will exist when the
    // component is created.
    const { project, dataset, resourceStates } = useRequestData();

    const { projectPath, datasetPath, canRoute } = useRoutes();
    const { tabPath, tabClass } = useTabs(datasetPath());
    return {
      project, dataset, ...resourceStates([project, dataset]),
      projectPath, datasetPath, tabPath, tabClass, canRoute
    };
  },
  created() {
    this.fetchData();
  },
  methods: {
    fetchProject(resend) {
      this.project.request({
        url: apiPaths.project(this.projectId),
        extended: true,
        resend
      }).catch(noop);
    },
    fetchDataset(resend) {
      this.dataset.request({
        url: apiPaths.dataset(this.projectId, this.datasetName),
        extended: true,
        resend
      }).catch(noop);
    },
    fetchData() {
      this.fetchProject(false);
      this.fetchDataset(false);
    }
  }
};
</script>

